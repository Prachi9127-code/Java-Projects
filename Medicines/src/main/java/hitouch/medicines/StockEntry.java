    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name=(String)jComboBox1.getSelectedItem();
        int qty=Integer.parseInt(jTextField4.getText());
         try{
       Connection conn=new GetConnection().getConnection();
       PreparedStatement ps=conn.prepareStatement("update medicines set available=available+? where name=? ");
       ps.setInt(1,qty);
       ps.setString(2,name);
       int x=ps.executeUpdate();
       if(x==1)
       {
           JOptionPane.showMessageDialog(null,"Stock Updated Successfully");
       }
       else
       {
            JOptionPane.showMessageDialog(null,"Stock Not Updated Successfully");
       }
      }
      catch(Exception e)
      {
          System.out.println(e);
      }
      
         jTextField4.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

