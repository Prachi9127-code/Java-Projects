    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=jTextField1.getText();
       String rate=jTextField3.getText();
       String qty=jTextField5.getText();
      try{
       Connection conn=new GetConnection().getConnection();
       PreparedStatement ps=conn.prepareStatement("insert into medicines values(?,?,?)");
       ps.setString(1,name);
       ps.setInt(2,Integer.parseInt(rate));
       ps.setInt(3,Integer.parseInt(qty));
       int x=ps.executeUpdate();
       if(x==1)
       {
           JOptionPane.showMessageDialog(null,"Record Inserted successfully");
       }
       else
       {
            JOptionPane.showMessageDialog(null,"Record Not Inserted successfully");
       }
      }
      catch(Exception e)
      {
          System.out.println(e);
      }
      jTextField1.setText("");
      jTextField3.setText("");
      jTextField5.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

